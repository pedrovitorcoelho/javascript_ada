// Trabalhando com operações matemáticas.

//Em Java script temos os seguinte operadores;

// adição:            +
// subtração:         -
// Multiplicação:     *
// Divisão:           /
// Exponenciação:     **
// Resto da divisão:  %


let number1 = 5;
let number2 = 2

console.log('soma = ', number1 + number2);
console.log('subtração = ', number1 - number2);
console.log('multiplicação = ', number1 * number2);
console.log('Exponenciação = ', number1 ** number2);
console.log('Resto da divisão = ', number1 % number2);


// É importante lembrar que na programação, seja qualquer linguagem, ela segue a ordem de calculo da matematica, ou seja:
